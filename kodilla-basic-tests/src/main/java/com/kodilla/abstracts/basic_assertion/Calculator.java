package com.kodilla.abstracts.basic_assertion;

public class Calculator {
    public int sum(int a, int b) { return a + b; }
    public int subtract(int a, int b) {
        return a - b;
    }
    public int squarePower(int a) {
        return a * a;
    }
    public double power(int a, int b) {
        return Math.pow(a, b);
    }
}
